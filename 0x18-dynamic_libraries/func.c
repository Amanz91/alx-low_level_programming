#include "lists.h"
/**
 * add - a func to add nums
 * @a: 1st arg
 * @b: 2nd arg
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - a func to subtract nums
 * @a: 1st arg
 * @b: 2nd arg
 * Return: difference
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * mul - a func to multiply nums
 * @a: 1st arg
 * @b: 2nd arg
 * Return: product
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * div - a func to divide nums
 * @a: 1st arg
 * @b: 2nd arg
 * Return: division
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mod - a func to divide nums and find reaminder
 * @a: 1st arg
 * @b: 2nd arg
 * Return: remainder
 */
int mod(int a, int b)
{
        return (a % b);
}
